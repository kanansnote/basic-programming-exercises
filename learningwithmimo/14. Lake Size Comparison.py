# Lake Size Comparison

sizes = [7340, 2117, 22300, 31700, 1679, 1014, 23000, 9910, 685]

largest = max(sizes)

print("Largest lake in sq mi:")
print(largest)

smallest = min(sizes)

print("Smallest lake in sq mi:")
print(smallest)

difference = largest - smallest
print("Difference between lakes in sq mi:")
print(difference)

